#include "libft.h"

void	ft_putnbr(int n)
{
	long long int nn;

	nn = (long long int)n;
	if (nn < 0)
	{
		ft_putchar('-');
		nn = -nn;
	}
	if (nn >= 10)
		ft_putnbr(nn / 10);
	ft_putchar((nn % 10) + '0');
}
